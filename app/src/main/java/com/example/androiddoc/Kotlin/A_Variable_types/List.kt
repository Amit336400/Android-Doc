package com.example.androiddoc.Kotlin.A_Variable_types

/***
 * Initialization List
 *
 * val list = listOf(1, 2, 3, 4)  // Immutable List
 */

/***
 * val list = listOf(10, 20, 30, 40, 50)
 *
 * // Access element at index 2
 * val element = list[2]  // This will be 30
 *
 * println(element)  // Output: 30
 *
 */


/***
 * MutableList (as a Dynamic Array)
 *
 * val mutableList = mutableListOf(1, 2, 3)  // Mutable List
 * mutableList.add(4)  // Adding elements
 * mutableList.remove(2)  // Removing elements
 */