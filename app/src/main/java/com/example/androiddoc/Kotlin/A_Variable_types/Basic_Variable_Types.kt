package com.example.androiddoc.Kotlin.A_Variable_types


/***
    val age: Int = 25         // Integer
    val price: Double = 12.99  // Double precision floating-point number
    val distance: Float = 10.5f  // Single precision floating-point number
    val isActive: Boolean = true  // Boolean (true or false)
    val initial: Char = 'A'     // Character
    val name: String = "John"   // String

 */

/**
 * val (Value): This keyword is used to declare read-only
 * (immutable) variablesOnce a val
 * is assigned to a val variable, it
 * cannot be changed. Attempting to reassign a val
 * variable will result in a compilation error.
 *
 * var (Variable): This keyword is used to declare mutable variables.
 * You can change the value of a var variable after it has been assigned.
 */